-- CON-201.拉取待下发处置清单 的固化查询（演示占位）。
-- 纪律：查询逻辑由工程侧维护，模型只能按接口名调用，永不改写（SPEC §4）。
-- param-less：本期需下发的处置清单为全量集（无命中式 WHERE :param）——
-- 参考实现 runner 对读步不穿透命名 params（历史缺口，见 W7-D0 §3.4 随行债），
-- 故固化查询不依赖运行时命名参数，直接返回本期全量清单。
-- 真实实现依部署环境的库表结构而定；此处为脱敏演示。
SELECT
    工单标题,
    目标单位,
    处置动作,
    建议完成日,
    经办人手机
FROM 待下发处置清单视图      -- 演示视图名
ORDER BY 目标单位;
